/**
 * A utility class containing constants representing keys for different intern
 * roles, such as engineering and business analyst. This class cannot be
 * instantiated.
 */
public final class InternKeys {

    /**
     * Private constructor to prevent instantiation of this utility class.
     */
    private InternKeys() {
    }

    /**
     * Key representing the engineering role.
     */
    public static final String ENGINEERING = "ENG";

    /**
     * Key representing the business analyst role.
     */
    public static final String BUSINESS_ANALYST = "BA";
}
